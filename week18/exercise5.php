5. Slumpa rutmönster
Skriv ett program som skriver ut 5x5 kvadrater i ett rutmönster. Rutorna kan vara div-taggar med en bakgrundsfärg satt. 
Programmet slumpar från en array med färger.
Utmaning 1: Se till att alla färger används lika många gånger.
Utmaning 2: Se till att två angränsande rutor inte har samma färg.
Utmaning 3: Se till att allt ovan fungerar oavsett hur lång arrayen av färger är (testa genom att ändra antalet färger i arrayen).
Utmaning 4: Ändra en siffra på ett ställe för att ändra storleken på rutmönstret
